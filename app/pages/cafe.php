<blockquote class="text-left">
    <p>
        Вы можете представить себе здоровый и приятный во всех отношениях отдых без правильного – а значит полезного и
        вкусного – питания? Мы тоже – нет! Для основного меню кафе "Шашлычный двор" были избраны любимые многими блюда
        классической грузинсуой кухни, однако не обошлось без элементов украинской кухни.
        Мы отдаем предпочтение натуральным продуктам, приобретаемым, преимущественно, на проверенных фермерских
        хозяйствах нашего региона. Ведь, как известно, блюда, приготовленные из качественных, свежих ингредиентов имеют
        повышенную энергетическую, питательную и витаминную ценность. А для того, чтобы вкушать трапезу – будь-то
        романтический ужин или семейный обед – было максимально приятно, мы создали в нашем ресторане особый антураж.
        Если же вы решили доверить нашему кафе организацию вашего торжества, мы постараемся в высшей мере
        соответствовать вашим представлениям об идеальном празднике.
    </p>
    <p>
        В течение всего дня в распоряжении гостей – легкие закуски и напитки (уж мы-то знаем, как может разыграться
        аппетит после пребывания на свежем воздухе и активного отдыха далеко за городом в Барабое!). А в теплую погоду
        вы сможете насладиться блюдами на мангале и гриле на открытой летней террасе.

    </p>
</blockquote>


